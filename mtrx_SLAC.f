      subroutine dtmnt(a,det)
      real*8 a(3,3),det,det1,det2,det3
c    
c    This subroutine calculates the determinant of a 
c    3 x 3 matrix.
c
c    a is a 3 x 3 double precision real matrix input
c    det is the d.p. real determinant output
c
      det1=(a(2,2)*a(3,3)-a(2,3)*a(3,2))*a(1,1)
      det2=(a(2,3)*a(3,1)-a(2,1)*a(3,3))*a(1,2)
      det3=(a(2,1)*a(3,2)-a(2,2)*a(3,1))*a(1,3)
      det=det1+det2+det3
      return
      end
