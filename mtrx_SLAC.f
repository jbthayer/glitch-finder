      subroutine lineq(a,b,x,nd,n,nr,aa,ierr)
      integer nd,n,nr,ierr,info
      integer ipvt(10)
      real*8 a(nd,n),b(nd,nr),x(nd,nr),aa(nd,n)
c
c   lineq solves the matrix linear equation a * x = b , by
c   finding x = inv(a) * b , where
c
c   a(n,n) is a real, double precision coefficient matrix
c   b(n,nr) is a real, d.p. right-hand-side array
c   x(n,nr) is a real, d.p. array for return of solution vectors
c   nd is the number of rows in dimension statement for a, b, x
c   n  is the number of equations to be solved
c   nr is the number of right-hand-side columns
c   aa(n,n) is a dummy array used internally (needs to be 
c      dimensioned in calling pgm; same dim as a)
c   ierr is an error code:  ierr=0 normal; ierr=1 means a is
c      singular
c
c   a and b are not destroyed
c
c   this routine calls on the LINPACK routines dgefa and dgesl
c
c   J. Arthur 6-10-87
c
      do 6 i=1,n
      do 5 j=1,n
    5 aa(i,j)=a(i,j)
      do 6 j=1,nr
    6 x(i,j)=b(i,j)
c
c   the matrix aa will be altered by dgefa
c   the matrix x will be altered by dgesl
c
      ierr=0
      call dgefa(aa,nd,n,ipvt,info)
      if(info .eq. 0) goto 10
      ierr=1
      return
   10 continue
      do 100 j=1,nr
      call dgesl(aa,nd,n,ipvt,x(1,j),0)
  100 continue
      return
      end
